def wrap_text(text, c, width, font_size):
    """Simple text wrapping"""
    c.setFont('Helvetica', font_size)
    words = text.split()
    lines = []
    current_line = []
    
    for word in words:
        test_line = ' '.join(current_line + [word])
        if c.stringWidth(test_line, 'Helvetica', font_size) <= width:
            current_line.append(word)
        else:
            if current_line:
                lines.append(' '.join(current_line))
            current_line = [word]
    
    if current_line:
        lines.append(' '.join(current_line))
    
    return lines

def draw_field_grid(c, y, fields):
    """Draw 2-column field grid"""
    col_width = CW / 2
    row_height = 13 * mm
    
    for row, (field1, value1, field2, value2) in enumerate(fields):
        row_y = y - (row * row_height)
        
        # Column 1
        c.setFont('Helvetica', 6.5)
        c.setFillColor(COLORS['TEXT_LIGHT'])
        c.drawString(ML, row_y - 3*mm, field1)
        
        c.setFont('Helvetica-Bold', 10)
        c.setFillColor(COLORS['DARK'])
        c.drawString(ML, row_y - 8.5*mm, value1)
        
        draw_line(c, ML, row_y - 10.5*mm, ML + col_width - 5*mm, row_y - 10.5*mm, COLORS['RULE'], 0.5)
        
        # Column 2
        c.setFont('Helvetica', 6.5)
        c.setFillColor(COLORS['TEXT_LIGHT'])
        c.drawString(ML + col_width, row_y - 3*mm, field2)
        
        c.setFont('Helvetica-Bold', 10)
        c.setFillColor(COLORS['DARK'])
        c.drawString(ML + col_width, row_y - 8.5*mm, value2)
        
        draw_line(c, ML + col_width, row_y - 10.5*mm, MR, row_y - 10.5*mm, COLORS['RULE'], 0.5)
    
    return y - (len(fields) * row_height)

