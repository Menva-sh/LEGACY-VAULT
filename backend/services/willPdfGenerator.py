def draw_rounded_rect(c, x, y, width, height, radius, fill_color=None, stroke_color=None, stroke_width=1):
    """Draw a rounded rectangle"""
    if fill_color:
        c.setFillColor(fill_color)
    if stroke_color:
        c.setStrokeColor(stroke_color)
    if stroke_width:
        c.setLineWidth(stroke_width)
    
    c.roundRect(x, y, width, height, radius, fill=(fill_color is not None), stroke=(stroke_color is not None))

def draw_circle(c, cx, cy, radius, fill_color=None, stroke_color=None, stroke_width=0):
    """Draw a circle"""
    if fill_color:
        c.setFillColor(fill_color)
